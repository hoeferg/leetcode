// O is the number of operations and n is the number that we are dealing with 
// It is porortional
//It can be O(2n) or any other but we always drop the constaint
public class Main {

    public static void printItems(int n) {
        for (int i = 0; i < n; i++) {
            System.out.println(i);
        }
    }



    // DO NOT CHANGE THE MAIN METHOD BELOW
    public static void main(String[] args) {
        printItems(10);
    }

}